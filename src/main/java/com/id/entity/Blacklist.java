package com.id.entity;

public class Blacklist {
    private int id;
    private int userid;
    private int blackid;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserid() {
        return userid;
    }

    public void setUserid(int userid) {
        this.userid = userid;
    }

    public int getBlackid() {
        return blackid;
    }

    public void setBlackid(int blackid) {
        this.blackid = blackid;
    }
}
